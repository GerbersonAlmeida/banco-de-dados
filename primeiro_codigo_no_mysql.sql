-- CRIA A TABELA BEBE
-- FOREIGN KEY - CRIA UMA CHAVE ESTRANGEIRA PRECISA USAR PARAMETROS PARA LINKAR AS CHAVES
-- REFERENCES - NECESSARIO UTILLIZAR ESTA FUNÇÃO PARA RELACIONAR UMA CHAVE PRIMARIA A UMA CHAVE SECUNDARIA NA TABELA
CREATE
	TABLE 	tb_bebe(
		cod_bebe INT PRIMARY KEY AUTO_INCREMENT UNIQUE,
        nome_bebe VARCHAR(255),
        data_nasc DATE,
        hr_nasc TIME,
        peso DECIMAL(6, 4),
        id_mae INT,
        id_medico INT,
        FOREIGN KEY (id_mae) REFERENCES tb_mae(cod_mae),
        FOREIGN KEY (id_medico) REFERENCES tb_medico(id_medico)
);


    