-- DROP DATABASE - DELETA UM BANCO
DROP
	DATABASE bercario;

-- CREATE DATABASE - CRIA UM BANCO
CREATE
	DATABASE bercario;

-- USE - UTILIZADO PARA INDICAR EM QUAL BANCO VAI SER CRIADO A TABELA
USE bercario;

-- CREATE TABLE - CRIA UMA TABELA ONDE CADA ITEM DENTRO DOS PARENTESES SÃO UMA COLUNA, TEM QUE ESPECIFICAR O FORMATO DE DADO DA COLUNA
-- PRIMARY KEY - DIZ QUE AQUELE ATRIBUTO É CHAVE PRIMARIA
-- AUTO INCREMENT - DIZ QUE AQUELE ATRIBUTO AUMENTA O NUMERO AUTOMATICAMENTE
-- NOT NULL - DIZ QUE AQUELE ATRIBUTO DEVE SER PREENCHIDO , NÃO PODE ESTAR VAZIO
-- UNIQUE - DIZ QUE SÓ PODE TER UM DAQUELE REGISTRO NA TABELA  
CREATE
	TABLE tb_mae (
		cod_mae INT PRIMARY KEY AUTO_INCREMENT,
        nm_mae VARCHAR(255) NOT NULL,
        tel_mae VARCHAR(20),
        email_mae VARCHAR(255) UNIQUE
);

CREATE
	TABLE tb_medico(
		id_medico INT PRIMARY KEY AUTO_INCREMENT,
        CRM VARCHAR(10) NOT NULL DEFAULT '123456-ES',
        nome_medico VARCHAR(255) NOT NULL
);

